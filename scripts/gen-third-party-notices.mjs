#!/usr/bin/env node
/**
 * Generate THIRD_PARTY_NOTICES.md from the actually-vendored assets.
 *
 * Self-hosting the in-browser WASM runtimes (Pyodide/DuckDB) means Hadrian now
 * *redistributes* them, which triggers each component's attribution obligation.
 * This enumerates the vendored Pyodide wheels from the lock file (so the list
 * stays correct across upgrades) and joins them with a static license map, then
 * emits the committed notices file. Run after `vendor-wasm.mjs`, then commit the
 * output (a static legal artifact — not built in CI).
 *
 *   node scripts/gen-third-party-notices.mjs
 */
import { existsSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const ROOT_DIR = dirname(SCRIPT_DIR);
const PYODIDE_DIR = join(ROOT_DIR, "ui/public/wasm/pyodide");
const LOCK = join(PYODIDE_DIR, "pyodide-lock.json");
const MARKER = join(PYODIDE_DIR, ".vendored-version");
const UI_PKG = join(ROOT_DIR, "ui/package.json");
const OUT = join(ROOT_DIR, "THIRD_PARTY_NOTICES.md");

if (!existsSync(LOCK)) {
  console.error(`[notices] ${LOCK} not found — run \`node scripts/vendor-wasm.mjs\` first.`);
  process.exit(1);
}

// SPDX license id per vendored Python package (keyed by lowercased name).
const WHEEL_LICENSES = {
  numpy: "BSD-3-Clause",
  pandas: "BSD-3-Clause",
  scipy: "BSD-3-Clause",
  "scikit-learn": "BSD-3-Clause",
  matplotlib: "matplotlib (BSD-style/PSF) — bundles DejaVu (Bitstream Vera/Arev) and STIX (OFL-1.1) fonts",
  pillow: "HPND (MIT-CMU)",
  contourpy: "BSD-3-Clause",
  cycler: "BSD-3-Clause",
  fonttools: "MIT",
  kiwisolver: "BSD-3-Clause",
  packaging: "Apache-2.0 OR BSD-2-Clause",
  pyparsing: "MIT",
  "python-dateutil": "Apache-2.0 OR BSD-3-Clause",
  pytz: "MIT",
  six: "MIT",
  joblib: "BSD-3-Clause",
  threadpoolctl: "BSD-3-Clause",
  micropip: "MPL-2.0",
};

const lock = JSON.parse(readFileSync(LOCK, "utf8"));
const pyodideVersion = existsSync(MARKER) ? readFileSync(MARKER, "utf8").trim() : "(unknown)";
const cpython = lock.info?.python ?? "(unknown)";
const uiPkg = JSON.parse(readFileSync(UI_PKG, "utf8"));
const dep = (n) => uiPkg.dependencies?.[n] ?? "(unknown)";

// Map vendored wheel filenames back to package name + version via the lock.
const byFile = new Map(Object.values(lock.packages).map((p) => [p.file_name, p]));
const vendoredWheels = readdirSync(PYODIDE_DIR).filter((f) => f.endsWith(".whl"));
const rows = vendoredWheels
  .map((f) => byFile.get(f))
  .filter(Boolean)
  .map((p) => ({ name: p.name, version: p.version, license: WHEEL_LICENSES[p.name.toLowerCase()] }))
  .sort((a, b) => a.name.localeCompare(b.name));

const missing = rows.filter((r) => !r.license).map((r) => r.name);
if (missing.length) {
  console.error(`[notices] No license mapping for: ${missing.join(", ")} — add them to WHEEL_LICENSES.`);
  process.exit(1);
}

const wheelTable = rows.map((r) => `| ${r.name} | ${r.version} | ${r.license} |`).join("\n");

const md = `# Third-Party Notices

Hadrian (licensed Apache-2.0 OR MIT) bundles and redistributes the third-party
components listed below to serve the in-browser tools (Python, SQL, charts)
without a runtime CDN dependency. Each component is distributed under its own
license; all are permissive. This file is generated by
\`scripts/gen-third-party-notices.mjs\` — do not edit by hand.

## Browser runtimes

| Component | Version | License | Source |
| --- | --- | --- | --- |
| Pyodide (loader + runtime) | ${pyodideVersion} | MPL-2.0 | https://github.com/pyodide/pyodide |
| CPython (bundled in Pyodide) | ${cpython} | PSF License Agreement | https://github.com/python/cpython |
| DuckDB-Wasm | ${dep("@duckdb/duckdb-wasm")} | MIT | https://github.com/duckdb/duckdb-wasm |
| quickjs-emscripten (QuickJS) | ${dep("quickjs-emscripten-core")} | MIT | https://github.com/justjake/quickjs-emscripten |
| Vega | ${dep("vega")} | BSD-3-Clause | https://github.com/vega/vega |
| Vega-Lite | ${dep("vega-lite")} | BSD-3-Clause | https://github.com/vega/vega-lite |
| Vega-Embed | ${dep("vega-embed")} | BSD-3-Clause | https://github.com/vega/vega-embed |
| sql.js | ${dep("sql.js")} | MIT | https://github.com/sql-js/sql.js |

**Pyodide / micropip (MPL-2.0):** redistributed unmodified. Source for the exact
version is available at the URL above; per MPL-2.0 §3.2 the corresponding source
is obtainable from that repository. No MPL-covered files have been modified.

**Fonts bundled inside matplotlib:** DejaVu Sans (Bitstream Vera / Arev fonts
license) and the STIX fonts (SIL Open Font License 1.1) are redistributed inside
the matplotlib wheel. Their full license texts are included in the wheel.

## Python packages (Pyodide wheels)

Vendored into \`ui/public/wasm/pyodide/\` and served same-origin. Versions and the
package set are resolved from \`pyodide-lock.json\` (Pyodide ${pyodideVersion}).

| Package | Version | License |
| --- | --- | --- |
${wheelTable}

_No copyleft beyond MPL-2.0's file-level obligation (Pyodide, micropip), which is
satisfied by unmodified redistribution plus this notice. None of the above affects
the licensing of Hadrian's own source._
`;

writeFileSync(OUT, md);
console.log(`[notices] Wrote ${OUT} (${rows.length} Python packages + 8 runtime components).`);
